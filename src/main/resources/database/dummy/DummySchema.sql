CREATE TEMPORARY TABLE IF NOT EXISTS temp_user_ids AS
SELECT
    id AS user_id,
    ROW_NUMBER() OVER (ORDER BY created_at) AS row_num
FROM
    users;

-- 2-2. 데이터 삽입에 필요한 변수를 설정
SET @user_count = (SELECT COUNT(*) FROM temp_user_ids);
SET @start_date = '2024-01-01 00:00:00.000000';
SET @end_date = '2025-12-03 23:59:59.999999';

-- 2-3. Reward와 Users 데이터를 JOIN하여 INSERT 준비 테이블 생성 (재참조 오류 회피)
-- 이 테이블을 사용하여 INSERT를 실행하면 'Can't reopen table' 오류를 피할 수 있습니다.
CREATE TEMPORARY TABLE IF NOT EXISTS temp_join_users AS
SELECT
    t1.project_id,
    t1.id AS reward_id,
    t1.price AS amount,
    t1.title AS order_name,
    t_u.user_id -- 실제 users 테이블에서 가져온 ID
FROM
    reward t1
        INNER JOIN
    (SELECT project_id, MIN(id) AS min_reward_id FROM reward GROUP BY project_id) t2
    ON
        t1.project_id = t2.project_id AND t1.id = t2.min_reward_id
        INNER JOIN
    temp_user_ids t_u
    ON
        t_u.row_num = ((t1.project_id - 1) % @user_count) + 1; -- 유저 ID 순환 할당

-- 2-4. Sponsorship 레코드 삽입 (temp_join_users 테이블 사용)
