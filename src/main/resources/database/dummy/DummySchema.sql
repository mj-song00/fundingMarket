    project_id, reward_id, user_id, amount, quantity, is_canceled,
    order_id, payment_key, order_name, method, status,
    sponsored_at, approved_at,
    created_at, updated_at
)
SELECT
    t_j.project_id,
    t_j.reward_id,
    t_j.user_id,
    t_j.amount,
    1 AS quantity,
    FALSE AS is_canceled,

    CONCAT('ORD_', LPAD(t_j.project_id, 5, '0'), '_', LPAD(t_j.reward_id, 5, '0'), '_', LPAD(FLOOR(RAND() * 99999), 5, '0')) AS order_id,
    CONCAT('PK_', LPAD(t_j.reward_id, 5, '0'), '_', LPAD(FLOOR(RAND() * 99999), 5, '0')) AS payment_key,
    t_j.order_name,
    '카드' AS method,
    'DONE' AS status,

    DATE_FORMAT(
            FROM_UNIXTIME(
                    UNIX_TIMESTAMP(@start_date) + FLOOR(RAND() * (UNIX_TIMESTAMP(@end_date) - UNIX_TIMESTAMP(@start_date) + 1))
            ),
            '%Y-%m-%d %H:%i:%s'
    ) AS sponsored_at,
    DATE_FORMAT(
            FROM_UNIXTIME(
                    UNIX_TIMESTAMP(@start_date) + FLOOR(RAND() * (UNIX_TIMESTAMP(@end_date) - UNIX_TIMESTAMP(@start_date) + 1))
            ),
            '%Y-%m-%d %H:%i:%s'
    ) AS approved_at,

    NOW(6) AS created_at,
    NOW(6) AS updated_at
FROM
    temp_join_users t_j
ORDER BY
    t_j.project_id;

